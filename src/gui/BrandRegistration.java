    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        String name = jTextField1.getText();
        String company = jComboBox1.getSelectedItem().toString();

        if (name.isBlank()) {
            JOptionPane.showMessageDialog(this, "Please enter category", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (company.equals("--Select--")) {
            JOptionPane.showMessageDialog(this, "Please select company", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {
            try {

                ResultSet rs1 = DBConnection.search("SELECT `id` FROM `company` WHERE `name`='" + company + "'");
                rs1.next();

                String company_id = rs1.getString("id");

                ResultSet rs = DBConnection.search("SELECT * FROM `brand` WHERE `name`='" + name + "' AND `company_id` = '" + company_id + "'");

                if (rs.next()) {
                    JOptionPane.showMessageDialog(this, "Brand Already exists", "Warning", JOptionPane.WARNING_MESSAGE);
                } else {
                    DBConnection.iud("INSERT INTO `brand`(`name`,`company_id`) VALUES('" + name + "','" + company_id + "')");
                    jTextField1.setText("");
                    jComboBox1.setSelectedIndex(0);
                    loadBrand();
                    jTextField1.grabFocus();
//                    JOptionPane.showMessageDialog(this, "Category Added Successfuly", "Success", JOptionPane.INFORMATION_MESSAGE);
                }
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_jButton4ActionPerformed

