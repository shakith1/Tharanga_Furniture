    public void loadCompany() {
        try {
            ResultSet rs = DBConnection.search("SELECT * FROM `company`");

            Vector v = new Vector();
            v.add("--Select--");

            while (rs.next()) {
                v.add(rs.getString("name"));
            }

            DefaultComboBoxModel dcm = new DefaultComboBoxModel(v);
            jComboBox1.setModel(dcm);

        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    public void loadBrand() {

        try {
            String query = "SELECT * FROM `brand` INNER JOIN `company` ON `brand`.`company_id` = `company`.`id` "
                    + "ORDER BY `brand`.`id` ASC";

            ResultSet rs = DBConnection.search(query);

            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            dtm.setRowCount(0);

            while (rs.next()) {
                Vector v = new Vector();
                v.add(rs.getString("brand.id"));
                v.add(rs.getString("brand.name"));
                v.add(rs.getString("company.name"));

                dtm.addRow(v);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }

    }

