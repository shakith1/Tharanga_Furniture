    public void loadCities() {
        try {
            ResultSet rs = DBConnection.search("SELECT * FROM `cities` ORDER BY `id` ASC");

            Vector v = new Vector();
            v.add("---City---");

            while (rs.next()) {
                v.add(rs.getString("name_en"));
            }

            DefaultComboBoxModel dcm = new DefaultComboBoxModel(v);
            jComboBox3.setModel(dcm);

        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    public void loadCompany() {

        try {
            String query = "SELECT * FROM `company` INNER JOIN `cities` ON `company`.`cities_id` = `cities`.`id` "
                    + "ORDER BY `company`.`id` ASC";

            ResultSet rs = DBConnection.search(query);

            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            dtm.setRowCount(0);

            while (rs.next()) {
                Vector v = new Vector();
                v.add(rs.getString("company.id"));
                v.add(rs.getString("company.name"));
                v.add(rs.getString("company.mobile"));
                v.add(rs.getString("company.email"));
                v.add(rs.getString("company.line1") + " " + rs.getString("company.line2") + " " + rs.getString("cities.name_en"));

                dtm.addRow(v);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }

    }

