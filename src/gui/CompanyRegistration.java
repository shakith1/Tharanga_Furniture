    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:

        String name = jTextField1.getText();
        String mobile = jTextField2.getText();
        String email = jTextField3.getText();
        String no = jTextField7.getText();
        String street = jTextField8.getText();
        String city = jComboBox3.getSelectedItem().toString();

        if (name.isBlank())
            JOptionPane.showMessageDialog(this, "Please enter Company Name", "Warning", JOptionPane.WARNING_MESSAGE);
        else if (mobile.isBlank())
            JOptionPane.showMessageDialog(this, "Please enter Company mobile Number", "Warning", JOptionPane.WARNING_MESSAGE);
        else if (!Pattern.compile("0[1-9][01245678][0-9]{7}").matcher(mobile).matches())
            JOptionPane.showMessageDialog(this, "Invalid Contact Number", "Warning", JOptionPane.WARNING_MESSAGE);
        else if (email.isBlank())
            JOptionPane.showMessageDialog(this, "Please enter Company email", "Warning", JOptionPane.WARNING_MESSAGE);
        else if (!Pattern.compile("^[A-Za-z0-9+_.-]+@(.+)$").matcher(email).matches())
            JOptionPane.showMessageDialog(this, "Invalid email address", "Warning", JOptionPane.WARNING_MESSAGE);
        else if (no.equals("No"))
            JOptionPane.showMessageDialog(this, "Please enter address no", "Warning", JOptionPane.WARNING_MESSAGE);
        else if (no.equals("Street"))
            JOptionPane.showMessageDialog(this, "Please enter address street", "Warning", JOptionPane.WARNING_MESSAGE);
        else if (city.equals("---City---"))
            JOptionPane.showMessageDialog(this, "Please select city", "Warning", JOptionPane.WARNING_MESSAGE);
        else {
            try {
                ResultSet rs1 = DBConnection.search("SELECT `id` FROM `cities` WHERE `name_en`='" + city + "'");
                rs1.next();

                String city_id = rs1.getString("id");

                DBConnection.iud("INSERT INTO `company`(`name`,`mobile`,`email`,`line1`,`line2`,`cities_id`) "
                        + "VALUES('" + name + "','" + mobile + "','" + email + "','" + no + "',"
                        + "'" + street + "'," + Integer.parseInt(city_id) + ")");

                resetFields();
                loadCompany();
                if(brand!=null)
                    brand.loadCompany();

            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

