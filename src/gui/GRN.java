    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        // TODO add your handling code here:
        String id = jLabel14.getText();
        String name = jLabel16.getText();
        String brand = jLabel18.getText();
        String category = jLabel20.getText();
        String qty = jTextField1.getText();
        String buying_price = jTextField2.getText();
        String selling_price = jTextField3.getText();

        Date mfd = jDateChooser1.getDate();

        if (jLabel6.getText().equals("None")) {
            JOptionPane.showMessageDialog(this, "Please Select Supplier", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (id.equals("None")) {
            JOptionPane.showMessageDialog(this, "Please Select Product", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (!Pattern.matches("[1-9][0-9]*", qty)) {
            JOptionPane.showMessageDialog(this, "Invalid Quantity", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (mfd == null) {
            JOptionPane.showMessageDialog(this, "Invalid Manufacture Date", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (mfd.after(new Date())) {
            JOptionPane.showMessageDialog(this, "Invalid Manufacture Date", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (buying_price.isBlank()) {
            JOptionPane.showMessageDialog(this, "Please enter buying price", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (selling_price.isBlank()) {
            JOptionPane.showMessageDialog(this, "Please enter selling price", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (Double.parseDouble(buying_price) >= Double.parseDouble(selling_price)) {
            JOptionPane.showMessageDialog(this, "Selling Price can't smaller than Buying Price", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {

            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();

            boolean isFound = false;
            int x = -1;

            for (int i = 0; i < dtm.getRowCount(); i++) {
                String pid = jTable1.getValueAt(i, 0).toString();

                if (pid.equals(id)) {
                    isFound = true;
                    x = i;
                    break;
                }
            }

            if (isFound) {
                int option = JOptionPane.showConfirmDialog(this, "This Product is already added. Do you want to update?", "Confirmation", JOptionPane.YES_OPTION, JOptionPane.INFORMATION_MESSAGE);

                if (option == JOptionPane.YES_OPTION) {
                    int oldVal = Integer.valueOf(jTable1.getValueAt(x, 3).toString());

                    int finalVal = Integer.parseInt(qty) + oldVal;

                    jTable1.setValueAt(String.valueOf(finalVal), x, 3);
                    jTable1.setValueAt(buying_price, x, 4);

                    double itemTotal = finalVal * Double.parseDouble(buying_price);
                    jTable1.setValueAt(String.valueOf(itemTotal), x, 7);

                    updateTotal();
                }

                resetFields();

            } else {
                Vector v = new Vector();
                v.add(id);
                v.add(name);
                v.add(brand);
                v.add(qty);
                v.add(buying_price);
                v.add(selling_price);
                v.add(sdf.format(mfd));

                double itemTotal = Integer.parseInt(qty) * Double.parseDouble(buying_price);
                v.add(df.format(itemTotal));

                dtm.addRow(v);
                updateTotal();
                resetFields();
            }
        }
    }//GEN-LAST:event_jButton6ActionPerformed

