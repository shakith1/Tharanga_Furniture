    public void validateAmount(KeyEvent evt, JTextField tf) {
        char c = evt.getKeyChar();
        String s = tf.getText();
        if (Character.isDigit(c)) {
            if (c == '0') {
                if (s.equals("0")) {
                    evt.consume();
                }
            }
        } else {
            switch (c) {
                case '.':
                    if (s.isEmpty()) {
                        tf.setText("0");
                    } else {
                        if (s.contains(".")) {
                            evt.consume();
                        }
                    }
                    break;
                default:
                    evt.consume();
                    break;
            }
        }
    }

