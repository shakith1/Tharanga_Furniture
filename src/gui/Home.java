    private void jButton8ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton8ActionPerformed
        // TODO add your handling code here:
        int confirm = JOptionPane.showConfirmDialog(this, "Do you want to sign out?", "Sign Out", JOptionPane.YES_NO_OPTION, JOptionPane.INFORMATION_MESSAGE);
        if (confirm == JOptionPane.YES_OPTION) {
            SignIn.name = "";
            SignIn.userType = 0;
            SignIn.userID = 0;

            SignIn s = new SignIn();
            this.dispose();
            s.setVisible(true);
        }
    }//GEN-LAST:event_jButton8ActionPerformed

