    public void getTime() {
        Thread t = new Thread(() -> {
            SimpleDateFormat sdf = new SimpleDateFormat("HH:mm:ss");

            while (true) {
                Date date = new Date();
                jLabel4.setText(sdf.format(date));
                try {
                    Thread.sleep(1000);
                } catch (InterruptedException ex) {
                    ex.printStackTrace();
                }
            }
        });
        t.start();
    }

