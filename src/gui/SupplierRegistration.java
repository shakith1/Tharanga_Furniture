    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:

        String fname = jTextField1.getText();
        String lname = jTextField2.getText();
        String mobile = jTextField3.getText();
        String email = jTextField4.getText();
        String category = jComboBox1.getSelectedItem().toString();
        String company_id = jLabel6.getText();

        if (fname.isBlank())
            JOptionPane.showMessageDialog(this, "Please enter First Name", "Warning", JOptionPane.WARNING_MESSAGE);
        else if (lname.isBlank())
            JOptionPane.showMessageDialog(this, "Please enter Last Name", "Warning", JOptionPane.WARNING_MESSAGE);
        else if (mobile.isBlank())
            JOptionPane.showMessageDialog(this, "Please enter contact number", "Warning", JOptionPane.WARNING_MESSAGE);
        else if (!Pattern.compile("07[01245678][0-9]{7}").matcher(mobile).matches())
            JOptionPane.showMessageDialog(this, "Invalid Contact Number", "Warning", JOptionPane.WARNING_MESSAGE);
        else if (email.isBlank())
            JOptionPane.showMessageDialog(this, "Please enter email", "Warning", JOptionPane.WARNING_MESSAGE);
        else if (!Pattern.compile("^[A-Za-z0-9+_.-]+@(.+)$").matcher(email).matches())
            JOptionPane.showMessageDialog(this, "Invalid email address", "Warning", JOptionPane.WARNING_MESSAGE);
        else if (category.equals("--Select--"))
            JOptionPane.showMessageDialog(this, "Please select category", "Warning", JOptionPane.WARNING_MESSAGE);
        else if (company_id.equals("None"))
            JOptionPane.showMessageDialog(this, "Please select company", "Warning", JOptionPane.WARNING_MESSAGE);
        else {
            try {
                ResultSet rs1 = DBConnection.search("SELECT `id` FROM `supplier_category` WHERE `name`='" + category + "'");
                rs1.next();

                String category_id = rs1.getString("id");

                DBConnection.iud("INSERT INTO `supplier`(`fname`,`lname`,`mobile`,`email`,`company_id`,`supplier_category_id`)"
                        + " VALUES('" + fname + "','" + lname + "','" + mobile + "','" + email + "',"
                        + "" + Integer.parseInt(company_id) + "," + Integer.parseInt(category_id) + ")");

                resetFields();
                loadSupplier();

            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

